#include <stdio.h>
#include <unistd.h>
#include <string.h>
/**********************************************************************************************************************
*  LOCAL MACROS CONSTANT\FUNCTION
*********************************************************************************************************************/



#define fd_stdout 	(1U)
/**********************************************************************************************************************
 *  LOCAL FUNCTIONS
 *********************************************************************************************************************/
int main(int argc, char *argv[])
{
    int i;
    for (i = 1; i < argc; i++) {

	write(fd_stdout, argv[i], strlen(argv[i]));
 	write(fd_stdout, " ", 1);
   
	}
	write (fd_stdout, "\n",1);
    return 0;
}

