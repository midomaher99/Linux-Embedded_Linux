#include "stdio.h"
#include "string.h"
#include "rand.h"
#include "fib.h"
#include "fact.h"


/**********************************************************************************************************************
 *  LOCAL MACROS CONSTANT\FUNCTION
 *********************************************************************************************************************/
#define SAME_STRING		(0UL)

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/
int main (void)
{
	char buffer[100]={ 0};

	while(1)
	{
		printf("Maher> ");
		fgets( buffer, 100,stdin);

		if(strcmp(buffer, "exit\n") == SAME_STRING	) 
		{
			printf("Good Bye :) \n");
			break;

		}
		else if(strcmp(buffer, "rand\n") == SAME_STRING	)
		{
			randCommand();
		}	
		else if(strcmp(buffer, "fib\n") == SAME_STRING	)	
		{
			fibCommand();
		}		
		else if(strcmp(buffer, "fact\n") == SAME_STRING	)	
		{
			factCommand();
		}		
		else
		{
			printf("Shell> %s",buffer);
		}
	}

	return 0;
}

